#include <iostream>
using namespace std;
int main()
{
    cout << "Defining value \n\n" << endl;
    
    cout << "First way" << endl;
    int a;
    
    a=10;
    
    cout << a << "\n" << endl;
    
    cout << "Second way" << endl;
    int b = 11;
    
    cout << b << "\n" << endl;
    
    cout << "Multiple way" << endl;
    int c =12, d = 13;
    
    cout << c << endl;
    cout << d << "\n" << endl;
    
    cout << "Updating" << endl;
    d = 14;
    
    cout << d << endl;
    
    return 0;
}
