#include <iostream>
using namespace std;
int main()
{
    cout << "Variable Types\n" << endl;
    
	//char
    char b = 'f';
    
    char s = 's' ;
    
    cout << "Char :" << endl;
    cout << b << endl;
    cout << s << "\n" << endl;
    
	//integer    
    int a = 50;
    
    int t;
    t = 60;
    
    cout << "Integer :" << endl;
    cout << a << endl;
    cout << t << "\n" << endl;
    
    //float-double-long double
    float pi = 3.14;
    
    double c = 21.5;
    
    long double x = 17.5132;
    
    cout << "Float :" << endl;
    cout << pi << "\n" << endl;
    
    cout << "Double :" << endl;
    cout << c << "\n" << endl;
    
    cout << "Long - Double :" << endl;
    cout << x << "\n" << endl;
    
    //boolean
    bool yil = true;
    
    bool deger = false;
    
    cout << "Boolean :" << endl;
    cout << yil << endl;
    cout << deger << endl;
    
    return 0;
}
